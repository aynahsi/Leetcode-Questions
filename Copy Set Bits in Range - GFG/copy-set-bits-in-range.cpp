class Solution{
    public:
    int setSetBit(int x, int y, int l, int r){
        // code here
        
        int digit = r-l+1;
        int o=l-1;
        int mask = ((1<<digit)-1)<<o;
        
       int ultimatey = y & mask;
       
       int ans = ultimatey | x;
       
       return ans ;
    }
};

